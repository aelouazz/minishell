#include "libft.h"

/*
** -----------------------------------------------------------------------------
** copies @src to @dst (including the '\0') and returns the @dst address       |
** -----------------------------------------------------------------------------
*/

char	*ft_strcpy(char *dst, const char *src)
{
	return (char*)((ft_memcpy((void*)dst, (void*)src, ft_strlen(src) + 1)));
}
