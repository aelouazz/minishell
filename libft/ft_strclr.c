#include "libft.h"

/*
** -----------------------------------------------------------------------------
** @s: The string that needs to be cleared. (set to '\0')                      |
** -----------------------------------------------------------------------------
*/

void		ft_strclr(char *s)
{
	while (s && *s)
		*(s++) = '\0';
}
