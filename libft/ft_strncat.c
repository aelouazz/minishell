#include "libft.h"

/*
** -----------------------------------------------------------------------------
** Append @s2 to the end of @s1     (untill @n is reached)    then adds '\0'   |
** -----------------------------------------------------------------------------
*/

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	j;

	i = 0;
	while (s1[i])
		i++;
	j = 0;
	while (s2[j] && j < n)
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
