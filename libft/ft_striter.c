#include "libft.h"

/*
** -----------------------------------------------------------------------------
** @s: The string to iterate                                                   |
** @f: The function to apply to each character of s                            |
** -----------------------------------------------------------------------------
*/

void		ft_striter(char *s, void (*f)(char *))
{
	if (!s || !f)
		return ;
	while (*s)
		f(s++);
}
