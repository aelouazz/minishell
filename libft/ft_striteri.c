#include "libft.h"

/*
** -----------------------------------------------------------------------------
** @s: The string to iterate                                                   |
** @f: The function to apply to each character of s and its index              |
** -----------------------------------------------------------------------------
*/

void				ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (!s || !(*s) || !f)
		return ;
	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		++i;
	}
}
