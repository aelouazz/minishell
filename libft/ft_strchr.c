#include "libft.h"

/*
** -----------------------------------------------------------------------------
** @RET: the address of the first occurence of @c in @s if it exists in it     |
** -----------------------------------------------------------------------------
*/

char	*ft_strchr(const char *s, int c)
{
	char *src;

	src = (char*)s;
	while (*src)
	{
		if (*src == c)
			return (src);
		++src;
	}
	if (c == 0)
		return (src);
	return (0);
}
