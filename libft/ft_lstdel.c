#include "libft.h"

/*
** -----------------------------------------------------------------------------
** clears all the linked list after applying @del to them                      |
** -----------------------------------------------------------------------------
*/

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *next;

	while ((*alst) && del)
	{
		next = (*alst)->next;
		ft_lstdelone(alst, del);
		*alst = next;
	}
	*alst = NULL;
}
