/*
** -----------------------------------------------------------------------------
** sets x to the specified interval without exceeding max or going bellow min   |
** -----------------------------------------------------------------------------
*/

double		ft_clamp(double x, double min, double max)
{
	if (x < min)
		x = min;
	else if (x > max)
		x = max;
	return (x);
}
