#include "libft.h"

/*
** -----------------------------------------------------------------------------
** @len: number of bytes of values @c to be written in the string @b           |
** -----------------------------------------------------------------------------
*/

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *ptr;

	ptr = (unsigned char *)b;
	while (len-- > 0)
		*(ptr++) = (unsigned char)c;
	return (b);
}
